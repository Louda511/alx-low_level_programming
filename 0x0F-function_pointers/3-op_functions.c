#include "calc.h"
#include <stdio.h>
#include <stdlib.h>

/**
 * op_add - adds two integers together
 * @a: first int
 * @b: second int
 *
 * Return: result of addition
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts b from a
 * @a: first int
 * @b: second int
 *
 * Return: result of addition
 */


int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply a and b
 * @a: first int
 * @b: second int
 *
 * Return: result of addition
 */


int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides a by b
 * @a: first int
 * @b: second int
 *
 * Return: result of addition
 */


int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - gets the remainder of the division of a by b
 * @a: first int
 * @b: second int
 *
 * Return: result of addition
 */



int op_mod(int a, int b)
{
	return (a % b);
}
